package org.example;

import com.fasterxml.jackson.annotation.JsonIgnore;

public class Person {
    @JsonIgnore
    private String name;
    private Integer age;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public Integer getAge() {
        return age;
    }

    public void setAge(Integer age) {
        this.age = age;
    }
}
